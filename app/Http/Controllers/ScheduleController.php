        $schedules = DB::table('schedules')
            ->leftJoin('selected_schedules', 'selected_schedules.schedule_id', 'schedules.id')
            ->select('schedules.id', 'schedules.name', 'selected_schedules.schedule_id AS isSelected')
            ->get();

